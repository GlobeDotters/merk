CONNECTION_SCRIPTS = []

def add_connection_script(hostid,script):
	entry = {}
	entry[hostid] = script
	CONNECTION_SCRIPTS.append(entry)

def get_connection_script(hostid):
	global CONNECTION_SCRIPTS
	output = ''

	for e in CONNECTION_SCRIPTS[:]:
		if hostid in e:
			output = e[hostid]
			CONNECTION_SCRIPTS.remove(e)
			break

	return output
